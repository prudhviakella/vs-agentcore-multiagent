# ── ECR helpers ───────────────────────────────────────────────────────────

def ecr_login(account_id):
    ecr_base = f"{account_id}.dkr.ecr.{REGION}.amazonaws.com"
    pwd = run(["aws", "ecr", "get-login-password", "--region", REGION], capture=True).stdout.strip()
    run(["docker", "login", "--username", "AWS", "--password-stdin", ecr_base], stdin_text=pwd)
    info(f"ECR login: {ecr_base}")
    return ecr_base

def ensure_ecr_repo(account_id, name):
    full = f"{PREFIX}/{name}"
    try:
        c("ecr").describe_repositories(repositoryNames=[full])
    except ClientError as e:
        if "RepositoryNotFoundException" in str(e):
            c("ecr").create_repository(repositoryName=full)
    return f"{account_id}.dkr.ecr.{REGION}.amazonaws.com/{full}"

# ── Wait helpers ──────────────────────────────────────────────────────────

def wait_lambda(name, retries=30, delay=3):
    print(f"  Waiting for {name}", end="", flush=True)
    for _ in range(retries):
        try:
            s = c("lambda").get_function(FunctionName=name)["Configuration"]["State"]
            if s == "Active":
                print(" \u2705")
                return True
        except ClientError:
            pass
        print(".", end="", flush=True)
        time.sleep(delay)
    print(" \u26a0\ufe0f  timeout")
    return False

def wait_runtime(runtime_id, retries=60, delay=5):
    print("  Waiting for READY", end="", flush=True)
    agc = c("bedrock-agentcore-control")
    for _ in range(retries):
        try:
            s = agc.get_agent_runtime(agentRuntimeId=runtime_id)["status"]
            if s == "READY":
                print(" \u2705")
                return True
        except ClientError:
            pass
        print(".", end="", flush=True)
        time.sleep(delay)
    print(" \u26a0\ufe0f  timeout — check AWS Console")
    return False

