        try:
            lam.remove_permission(FunctionName=func, StatementId=stmt)
        except ClientError:
            pass
        try:
            lam.add_permission(FunctionName=func, StatementId=stmt,
                               Action="lambda:InvokeFunction",
                               Principal="bedrock-agentcore.amazonaws.com")
            ok(f"Lambda permission: {func}")
        except ClientError as e:
            warn(f"Lambda permission failed {func}: {e}")

